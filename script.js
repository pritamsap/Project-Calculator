

// basic math operators

function addition(num1, num2) {
    return num1 + num2;
}


function subtraction(num1, num2) {
    return num1 - num2;
}


function multplication(num1, num2) {
    return num1 * num2;
}


function division(num1, num2) {
    return num1 / num2;
}


